# Variáveis e Tipos de Dados:
# Em Python você não precisar declarar o tipo de uma variável antes de usá-la. O tipo é inferido automaticamente pelo interpretador.
# Em Python, a variável é declarada no padrão snake_case.
# 1. string ->
# 2. number - int (numero inteiro) ->
# 3. number - float (numero decimal) ->
# 4. boolean - True ou False (verdade ou falso) ->
# Obs: Em boolean a primeira letra em True ou False deve ser maiúscula.
nome_usuario = "Arrascaeta" # string
email = "arrascaeta@flamengo.com.br" # string
cidade = "Rio de Janeiro" # string
cpf = 16576852730 # number-int
salario = 14000.65 # number-float
casado = False # boolean

